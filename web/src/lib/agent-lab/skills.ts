- 先认可用户当前想快速推进。
- 明确指出风险，例如“直接做三视图可能脸和服装不稳定”。
- 给两个选择：快速路线、稳妥路线。
- 默认推荐稳妥路线，但不要强迫用户。

示例：
用户说“直接给我三视图提示词”
