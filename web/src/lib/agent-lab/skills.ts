export function buildAgentLabMessages(
    messages: Array<{ role: "user" | "assistant"; content: string }>,
    options?: {
        personaPrompt?: string;
        memoryPrompt?: string;
    },
) {
    return [
        { role: "system", content: AGENT_LAB_SYSTEM_PROMPT },
        ...(options?.personaPrompt?.trim() ? [{ role: "system", content: `当前会话的 Agent 身份设定：\n${options.personaPrompt.trim()}` }] : []),
        ...(options?.memoryPrompt?.trim()
            ? [
                  {
                      role: "system",
                      content: `当前会话的项目记忆。回答时优先使用这些背景，不要要求用户重复提供；如果用户新输入与记忆冲突，以用户最新输入为准：\n${options.memoryPrompt.trim()}`,
                  },
              ]
            : []),
        ...messages.slice(-12).map((message) => ({
            role: message.role,
            content: message.content,
        })),
    ];
}

